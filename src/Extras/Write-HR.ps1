function Write-HR {
    Write-CharRepeater "-" $Global:BUFFER_WIDTH -fg "Black";
    Write-NewLine;
}
#-----------------------------------------------------------------------
#-----------------------------------------------------------------------
#-----------------------------------------------------------------------
function Write-HR-DarkGreen {
    Write-CharRepeater "-" $Global:BUFFER_WIDTH -fg "DarkGreen";
    Write-NewLine;
}
