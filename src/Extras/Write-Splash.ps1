function Write-Splash([string] $msg) {
    Write-Host "== " -ForegroundColor DarkBlue -NoNewline;
    Write-Host $msg -ForegroundColor Cyan -NoNewline;
    Write-Host " ==" -ForegroundColor DarkBlue;
    Write-HR;
}
