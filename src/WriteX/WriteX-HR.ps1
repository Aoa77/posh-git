function WriteX-HR {
    WriteX-CharRepeater "-" $Global:BUFFER_WIDTH -fg "Black";
    WriteX-NewLine;
}
#-----------------------------------------------------------------------
#-----------------------------------------------------------------------
#-----------------------------------------------------------------------
function WriteX-HR-DarkGreen {
    WriteX-CharRepeater "-" $Global:BUFFER_WIDTH -fg "DarkGreen";
    WriteX-NewLine;
}
